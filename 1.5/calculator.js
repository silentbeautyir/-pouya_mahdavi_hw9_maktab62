let calculator = {
  add: (num1, num2) => {
    return num1 + num2;
  },
  minus: (num1, num2) => {
    return num1 - num2;
  },
  Multiplication: (num1, num2) => {
    return num1 * num2;
  },
  Division: (num1, num2) => {
    return num1 / num2;
  },
};
module.exports = calculator;
